    if score <= 15:
        profile = "🌱 Новичок: Закладываем фундамент"
        advice = f"Привет, {name}! Сейчас твоими финансами управляют эмоции и случайности. И это нормально, с этого начинают все.\n\n**Твой фокус на ближайший месяц:** Начни с простого - веди учёт трат хотя бы 2 недели. Это покажет, где утекают деньги. Параллельно попробуй откладывать хотя бы 1000 руб в месяц на отдельный счёт. Не думай пока про сложные инструменты - твоя задача N1 - создать фундамент."

    elif score <= 21:
        profile = "👀 Осознанный: Строим систему"
        advice = f"Отличная работа, {name}! Ты уже понимаешь, что деньги любят счёт, и чувствуешь, что инфляция съедает сбережения. Но тебе пока не хватает системности.\n\n**Твой фокус на ближайший месяц:** Автоматизируй накопления. Настрой автоперевод 10% от зарплаты в день поступления. Поставь конкретную цель с суммой и сроком. Изучи простые инструменты: вклады, накопительные счета, короткие облигации."

    else:
        profile = "🧠 Опытный: Масштабируем результат"
        advice = f"Поздравляем, {name}! У тебя уже есть отличная база: система, контроль долгов и понимание рынка.\n\n**Твой фокус на ближайший месяц:** Проанализируй свой текущий портфель. Проверь, соответствует ли он твоим целям. Изучи налоговые вычеты и способы оптимизации комиссий. Возможно, пора диверсифицировать активы."

    st.success(f"### Твой профиль: {profile}")
    st.markdown(advice)
    st.markdown("---")

    st.markdown("### Что дальше?")
    st.markdown("Твой первый блок 'Жизнь и принципы' почти пройден. Ты можешь углубиться в тему или перейти к практике.")

    col1, col2 = st.columns(2)
    with col1:
        st.button("📊 Модуль 2: Тест 'Умеете ли вы копить?'", disabled=True, help="Скоро будет доступно!")
        st.button("📚 Модуль 3: Принципы накопления", disabled=True, help="Скоро будет доступно!")
    with col2:
        st.button("🧩 Модуль 4: 7 навыков Кови", disabled=True, help="Скоро будет доступно!")
        st.button("🔄 Пройти тест заново", on_click=lambda: st.session_state.update({'current_step': 'welcome', 'answers': {}}))

    st.markdown("---")
    st.markdown("*💡 Создано с заботой командой Double K. Помни: этот тест - не инвестиционная рекомендация, а повод задуматься о своих привычках.*")
