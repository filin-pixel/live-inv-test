if 'answers' not in st.session_state:
    st.session_state.answers = [None] * len(questions)

for i, q in enumerate(questions):
    st.markdown(f"**{q['question']}**")
    st.session_state.answers[i] = st.radio(
        "Выбери один вариант:",
        q['options'],
        key=f"q_{i}",
        label_visibility="collapsed"
    )
    st.markdown("---")

if st.button("Узнать мой профиль", type="primary", use_container_width=True):
    if None in st.session_state.answers:
        st.error("Пожалуйста, ответь на все вопросы!")
    else:
        total_score = sum([st.session_state.answers.index(ans) + 1 for ans in st.session_state.answers])
        
        if total_score <= 7:
            profile = "🌱 Новичок в зоне риска"
