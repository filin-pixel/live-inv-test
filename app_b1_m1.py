
import streamlit as st

# Настройка страницы
st.set_page_config(page_title="Double K: Жизнь и Принципы", page_icon="🧠", layout="centered")

# Инициализация состояния сессии
if 'user_name' not in st.session_state:
    st.session_state.user_name = ""
if 'current_step' not in st.session_state:
    st.session_state.current_step = "welcome"
if 'answers' not in st.session_state:
    st.session_state.answers = {}

# === ЭКРАН 1: ПРИВЕТСТВИЕ И АВТОРИЗАЦИЯ ===
if st.session_state.current_step == "welcome":
    st.title("🧠 Double K")
    st.markdown("### Простой путь к финансовой уверенности")
    st.markdown("Мы не будем грузить тебя сложными терминами. Только честные вопросы, принципы и конкретные шаги.")
    st.markdown("---")
    
    st.markdown("#### Как к тебе обращаться?")
    name_input = st.text_input("Введи своё имя", placeholder="Например, Клим")
    
    if st.button("Начать путь", type="primary", use_container_width=True):
        if name_input.strip():
            st.session_state.user_name = name_input.strip()
            st.session_state.current_step = "module1"
            st.rerun()
        else:
            st.warning("Пожалуйста, введи имя, чтобы мы могли персонализировать ответы.")

# === ЭКРАН 2: МОДУЛЬ 1 (ПРОФИЛЬ И ЦЕЛИ) ===
elif st.session_state.current_step == "module1":
    st.title(f"Привет, {st.session_state.user_name}! 👋")
    st.markdown("### Модуль 1: Профиль и цели")
    st.markdown("Ответь на 5 честных вопросов. Это поможет нам понять, где ты сейчас, и дать именно те советы, которые тебе нужны.")
    st.markdown("---")

    questions = [
        {
            "id": "q1",
            "text": "1. Как бы ты описал(а) свои отношения с деньгами прямо сейчас?",
            "options": {
                "a": "Трачу всё под ноль, не понимаю, куда они уходят.",
                "b": "Есть небольшие сбережения, но я не знаю, что с ними делать, боюсь инфляции.",
                "c": "Я уже откладываю или инвестирую, но хочу сделать это более системно."
            }
        },
        {
            "id": "q2",
            "text": "2. Представь: ты отложил(а) 50 000 руб на важную цель. Появляется возможность вложить их так, чтобы они могли удвоиться, но есть риск потерять 20%. Твои действия?",
            "options": {
                "a": "Ни за что! Для меня важнее сохранить, чем заработать.",
                "b": "Разделю: половину оставлю в безопасности, половину попробую рискнуть.",
                "c": "Готов(а) рискнуть, если понимаю, во что вкладываю и почему это сработает."
            }
        },
        {
            "id": "q3",
            "text": "3. Как ты обычно принимаешь финансовые решения (покупки, траты)?",
            "options": {
                "a": "Спонтанно: увидел(а) -> захотел(а) -> купил(а).",
                "b": "Иногда думаю пару дней, но часто поддаюсь настроению или скидкам.",
                "c": "У меня есть бюджет или чёткие правила, которым я стараюсь следовать."
            }
        },
        {
            "id": "q4",
            "text": "4. Что ты знаешь про инфляцию?",
            "options": {
                "a": "Знаю, что цены растут, но не понимаю, как это лично на меня влияет.",
                "b": "Понимаю, что деньги на карте обесцениваются, и стараюсь их куда-то пристраивать.",
                "c": "Знаю конкретные инструменты, которые помогают защитить деньги от инфляции."
            }
        },
        {
            "id": "q5",
            "text": "5. Есть ли у тебя чёткая финансовая цель?",
            "options": {
                "a": "Нет, живу от зарплаты до зарплаты.",
                "b": "Есть общая идея (например, 'хочу накопить на машину'), но без конкретных цифр и сроков.",
                "c": "Да, есть конкретная сумма и срок (например, '500 000 руб к декабрю 2026 года')."
            }
        }
    ]

    for q in questions:
        st.markdown(f"**{q['text']}**")
        selected = st.radio(
            "Выбери один вариант:",
            options=list(q['options'].values()),
            key=q['id'],
            label_visibility="collapsed"
        )
        for key, value in q['options'].items():
            if value == selected:
                st.session_state.answers[q['id']] = key
        st.markdown("---")

    if st.button("Узнать мой профиль", type="primary", use_container_width=True):
        if len(st.session_state.answers) < 5:
            st.warning("Пожалуйста, ответь на все 5 вопросов!")
        else:
            st.session_state.current_step = "results"
            st.rerun()

# === ЭКРАН 3: РЕЗУЛЬТАТЫ ===
elif st.session_state.current_step == "results":
    name = st.session_state.user_name
    answers = st.session_state.answers
    
    score = sum(1 if ans == 'a' else 2 if ans == 'b' else 3 for ans in answers.values())
    
    st.title(f"Готово, {name}! 🎉")
    
    if score <= 8:
        profile = "🌱 Начинающий: Фундамент"
        advice = f"Привет, {name}! Сейчас твоими финансами управляют эмоции и случайности. И это нормально, с этого начинают все. По принципу Рэя Далио: 'Прими реальность и работай с ней'.\n\n**Твой фокус на ближайший месяц:** Не думай пока про сложные инвестиции. Твоя задача N1 - начать вести простой учёт трат и создать первую 'подушку безопасности' хотя бы на 1 месяц жизни. Мы в Double K поможем тебе найти эти деньги в бюджете."
        
    elif score <= 12:
        profile = "👀 Осознанный: Поиск системы"
        advice = f"Отличная работа, {name}! Ты уже понимаешь, что деньги любят счёт, и чувствуешь, что инфляция съедает сбережения. Но тебе пока не хватает чёткой системы.\n\n**Твой фокус на ближайший месяц:** Внедрить 'Принцип Машины'. Настрой автоматическое откладывание даже 5-10% от любого дохода в день зарплаты. Сделай так, чтобы правильные решения принимались без твоего участия и силы воли."
        
    else:
        profile = "🧠 Стратег: Масштабирование"
        advice = f"Поздравляем, {name}! Ты мыслишь системно. Ты понимаешь последствия второго порядка, не боишься смотреть правде в глаза и уже строишь свои финансовые правила.\n\n**Твой фокус на ближайший месяц:** Проанализировать текущие инструменты. Возможно, твой портфель слишком консервативен или, наоборот, перегрет. Пора оптимизировать налоги, комиссии и ребалансировать активы."

    st.success(f"### Твой профиль: {profile}")
    st.markdown(advice)
    st.markdown("---")
    
    st.markdown("### Что дальше?")
    st.markdown("Твой первый блок 'Жизнь и принципы' почти пройден. Ты можешь углубиться в тему или перейти к практике.")
    
    col1, col2 = st.columns(2)
    with col1:
        st.button("📊 Модуль 2: Тест 'Умеете ли вы копить?'", disabled=True, help="Скоро будет доступно!")
        st.button("📚 Модуль 3: Принципы накопления", disabled=True, help="Скоро будет доступно!")
    with col2:
        st.button("🧩 Модуль 4: 7 навыков Кови", disabled=True, help="Скоро будет доступно!")
        st.button("🔄 Пройти тест заново", on_click=lambda: st.session_state.update({'current_step': 'welcome', 'answers': {}}))
        
    st.markdown("---")
    st.markdown("*💡 Создано с заботой командой Double K. Помни: этот тест - не инвестиционная рекомендация, а повод задуматься о своих привычках.*")
